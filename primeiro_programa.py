curso = "python"

print(curso.upper()) # maiusculo
print(curso.lower()) # minusculo
print(curso.title()) # Primeiro maisculo resto minusculo
print(curso.strip()) # Remove espaços em branco
print(curso.lstrip()) # esquerda
print(curso.rstrip()) # direita

#Junções  e centralização

print(curso.center(10,"#")) #total caracteres igual 10
print(".".join(curso)) 


